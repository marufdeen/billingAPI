/**
* @description check valid name
*/
export const validName = /^[a-zA-z]{2,20}$/;

/**
* @description check valid number
*/
export const validNumber = /^[0-9]*$/;

/**
* @description check valid phone number
*/
export const validPhoneNumber = /^[0-9]{11}$/;

/**
 * @description check valid email
*/
export const validEmail = /^([a-zA-Z0-9_\-\.]+)@([a-zA-Z0-9_\-\.]+)\.([a-zA-Z]{2,5})$/;

 
 